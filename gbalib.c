void Init_GBA(uint8_t mode)
{
	static volatile uint16_t * const reg_disp_ctl = (void*)0x4000000;
	*reg_disp_ctl = mode | BG0_ENABLE | BG2_ENABLE | ENABLE_OBJECTS | MAPPINGMODE_1D;
	*reg_disp_ctl &= ~FRAME_SEL_BIT;
}



static inline void drawChar(char ch, uint8_t x, uint8_t y, uint8_t color_index, uint8_t color_bg)
{
    unsigned char i, j;
    
    if (ch < 33) return;
    
	ch -= 33;
    const unsigned char *charSprite = (ch << 3) + std_font;
    
	uint16_t *base = &fb[(y * M4_WIDTH) >> 1];
    // Draw charSprite as monochrome 8*8 image using given color
    for (j = 0; j < 8; j += 2)
    {
        uint16_t *line = base + ((x + j) >> 1);
        uint8_t bit1 = 7 - j;
        uint8_t bit2 = bit1 - 1;
        for (i = 0; i < 8; i++)
        {
            uint8_t clrid1 = ((charSprite[i] >> bit1) & 1) ? color_index : color_bg;
            uint8_t clrid2 = ((charSprite[i] >> bit2) & 1) ? color_index : color_bg;
            line[(i << 7) - (i << 3)] = clrid1 | (clrid2 << 8);
        }
    }
}


void Draw_Text(const char* str, uint8_t x, uint8_t y, uint8_t color_index, uint8_t color_bg)
{
	//ERAPI_DrawText( region, 0x50, 0x08, "Sex");
	uint8_t i;
	for(i = 0; str[i]; i++)
	{
		drawChar(str[i], x + (i << 3), y, color_index, color_bg);
	}
}

void Draw_Text_Center(const char* str, uint8_t y, uint8_t color_index, uint8_t color_bg)
{
	unsigned char i;
	uint8_t x;
	x = (240 - (strlen(str) << 3)) >> 1;
	Draw_Text(str, x, y, color_index, color_bg);
}

void *memset16(void *dst, uint16_t val, size_t count) {
    uint16_t *dst16 = (uint16_t*)dst;
    uint16_t val16 = (uint16_t)val;
    while (count--) {
        *dst16++ = val16;
    }
    return dst;
}


// Assuming imgBuffer is your image and screenBuffer is your screen
void drawImage(const unsigned char* imgBuffer, uint8_t imageWidth, uint8_t imageHeight, uint8_t posX, uint8_t posY) 
{
    uint32_t sizew = imageWidth / 4;

    for (uint8_t y = 0; y < imageHeight; y++) 
    {
        // Add the Y position offset to the destination address
        uint16_t* dest = &fb[(y + posY) * 120 + (posX>>1)];
        const unsigned char* source = &imgBuffer[y * imageWidth];

        // Copy one line of the image into the correct position in the buffer
        DMAFastCopy(source, dest, sizew, DMA_32NOW);
    }
}

